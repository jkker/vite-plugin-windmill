import { mkdir, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

import ts from "typescript";

const sourceUrl =
  "https://raw.githubusercontent.com/windmill-labs/windmill/main/frontend/src/lib/rawAppWmillTs.ts";

const currentFile = fileURLToPath(import.meta.url);
const packageRoot = path.resolve(path.dirname(currentFile), "..");
const outputPath = path.join(packageRoot, "src/generated/upstream-build-runtime.ts");

const response = await fetch(sourceUrl, {
  headers: {
    "user-agent": "vite-plugin-windmill-sync-runtime",
  },
});

if (!response.ok)
  throw new Error(`Failed to fetch ${sourceUrl}: ${response.status} ${response.statusText}`);

const source = await response.text();
const transpiled = ts.transpileModule(source, {
  compilerOptions: {
    module: ts.ModuleKind.ESNext,
    newLine: ts.NewLineKind.LineFeed,
    removeComments: false,
    target: ts.ScriptTarget.ES2020,
  },
});

const normalizedSource = transpiled.outputText.trimEnd() + "\n";
const generatedFile = `// Generated by scripts/sync-upstream-runtime.mjs
// Source: ${sourceUrl}

export const UPSTREAM_RAW_APP_WMILL_TS_URL = ${JSON.stringify(sourceUrl)}
export const buildRuntimeSource = ${JSON.stringify(normalizedSource)}
`;

await mkdir(path.dirname(outputPath), { recursive: true });
await writeFile(outputPath, generatedFile);

console.log(`Updated ${path.relative(packageRoot, outputPath)} from upstream Windmill source`);
